package btth_ss13;

public class Patient {
    private String id;
    private String name;
    private int age;
    private String gender;
    private  String pathology;

    public Patient() {

    }

    public Patient(String id, String name, int age, String gender, String pathology) {
        this.id = id;
        this.name = name;
        this.age = age;
        this.gender = gender;
        this.pathology = pathology;
    }

    public String getId() {
        return id;
    }

    public void setId(String id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }

    public String getGender() {
        return gender;
    }

    public void setGender(String gender) {
        this.gender = gender;
    }

    public String getPathology() {
        return pathology;
    }

    public void setPathology(String pathology) {
        this.pathology = pathology;
    }
}
